import random
import numpy as np

def random_class():
    return random.randint(0, 1)


m = 4
n = 50

listX = []
for i in range(n):
    temp = []
    for j in range(m):
        temp.append(random_class())
    listX.append(temp)

X = np.asarray(listX)

listY = []
for i in range(n):
    listY.append(random_class())

Y = np.asarray(listY)

